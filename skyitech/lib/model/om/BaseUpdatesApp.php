	public function buildCriteria()
	{
		$criteria = new Criteria(UpdatesAppPeer::DATABASE_NAME);

		if ($this->isColumnModified(UpdatesAppPeer::ID)) $criteria->add(UpdatesAppPeer::ID, $this->id);
		if ($this->isColumnModified(UpdatesAppPeer::TITLE)) $criteria->add(UpdatesAppPeer::TITLE, $this->title);
		if ($this->isColumnModified(UpdatesAppPeer::DESCRIPTION)) $criteria->add(UpdatesAppPeer::DESCRIPTION, $this->description);
		if ($this->isColumnModified(UpdatesAppPeer::TID)) $criteria->add(UpdatesAppPeer::TID, $this->tid);
		if ($this->isColumnModified(UpdatesAppPeer::TYPE)) $criteria->add(UpdatesAppPeer::TYPE, $this->type);
		if ($this->isColumnModified(UpdatesAppPeer::THUMB)) $criteria->add(UpdatesAppPeer::THUMB, $this->thumb);
		if ($this->isColumnModified(UpdatesAppPeer::STATUS)) $criteria->add(UpdatesAppPeer::STATUS, $this->status);
		if ($this->isColumnModified(UpdatesAppPeer::CREATED_AT)) $criteria->add(UpdatesAppPeer::CREATED_AT, $this->created_at);

		return $criteria;
	}

	
